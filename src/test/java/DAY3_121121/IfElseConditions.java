package DAY3_121121;

public class IfElseConditions {
    public static void main(String[] args) {

        //define the integer variables
        int a = 3;
        int b = 2;

        // verify if a is less than b then print
        if(a<b){
            System.out.println("a is less than b");// it is not going to print anything because a is not less than b so we have to use the conditional statement
        } else {
            System.out.println("a is either greater or equal to b");// with the else condition we do not know either the value is equal or greater but we know that one of the 2 statements is true.

        }// end of if else
    }// end of main method
}// end of java class

